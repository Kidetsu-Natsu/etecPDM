//Aula 10: Operadoers de comparação e igualdade
//operadores Lógicos e Operadores de intervalo

fun main()
{
    var n = 7

    if(n > 5) println("$n é maior que 5")
    if(n < 9) println("$n é menor que 9")
    if(n >= 6) println("$n é maior ou igual a 6")
    if(n <= 7) println("$n é menor ou igual a 7")

    if(n == 7) println("$n é igual a 7")
    if(n != 4) println("$n é diferente de 4")
    if(!(n == 10)) println("$n não é igual 10")


}


